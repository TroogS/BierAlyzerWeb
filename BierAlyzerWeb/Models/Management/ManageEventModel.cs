using System;
using System.ComponentModel.DataAnnotations;
using Contracts.Model;

namespace BierAlyzerWeb.Models.Management
{
    public class ManageEventModel
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the event. </summary>
        /// <value> The identifier of the event. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public Guid EventId { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name. </summary>
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        [Required]
        public string Name { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the description. </summary>
        /// <value> The description. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public string Description { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the code. </summary>
        /// <value> The code. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public string Code { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the start. </summary>
        /// <value> The start. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        [Required]
        [DataType(DataType.DateTime)]
        public DateTime Start { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the end. </summary>
        /// <value> The end. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        [Required]
        [DataType(DataType.DateTime)]
        public DateTime End { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the type. </summary>
        /// <value> The type. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public EventType Type { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the status. </summary>
        /// <value> The status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public EventStatus Status { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of users. </summary>
        /// <value> The number of users. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public int UserCount { get; set; }
    }
}